import Notice from "../models/notification.js";
import Task from "../models/task.js";
import User from "../models/user.js";

export const createTask = async (req, res) => {
  try {
    const { userId } = req.user;

    const { title, team, stage, date, priority, assets } = req.body;

    let text = "تمت إضافه مهمه جديده لك";
    if (team?.length > 1) {
      text = text + ` و ${team?.length - 1} أخرين.`;
    }

    text =
      text +
      ` أولويه هذه المهمه هي اولويه ${priority} , لذالك وجب الانجاز وفق ذالك. تاريخ المهمه ${new Date(
        date
      ).toDateString()}. شكرا لك!!!`;

    const activity = {
      type: "assigned",
      activity: text,
      by: userId,
    };

    const task = await Task.create({
      title,
      team,
      stage: stage.toLowerCase(),
      date,
      priority: priority.toLowerCase(),
      assets,
      activities: activity,
    });

    await Notice.create({
      team,
      text,
      task: task._id,
    });

    res
      .status(200)
      .json({ status: true, task, message: "تم إضافه المهمه بنجاح." });
  } catch (error) {
    console.log(error);
    return res.status(400).json({ status: false, message: error.message });
  }
};

